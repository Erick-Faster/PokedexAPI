CREATE DATABASE POKEDEX
GO

USE POKEDEX
GO

CREATE TABLE TRAINER(
	IDTRAINER INT PRIMARY KEY IDENTITY,
	NOME VARCHAR(30) NOT NULL,
	SEXO CHAR(1) NOT NULL,
	EMAIL VARCHAR(30) UNIQUE NOT NULL,
	LIGA VARCHAR(20)
)
GO


ALTER TABLE TRAINER
ADD CONSTRAINT CK_SEXO CHECK (SEXO IN('M', 'F'))
GO

CREATE TABLE ENDERECO(
	
	IDENDERECO INT PRIMARY KEY IDENTITY,

	BAIRRO VARCHAR(50) NOT NULL,
	ESTADO CHAR(2) NOT NULL,
	PAIS VARCHAR(20) NOT NULL,

	ID_TRAINER INT UNIQUE,

)
GO

ALTER TABLE ENDERECO ADD CONSTRAINT FK_ENDERECO_TRAINER
FOREIGN KEY (ID_TRAINER)
REFERENCES TRAINER(IDTRAINER)
GO

CREATE TABLE POKEDEXINFO(

	IDPOKEDEX INT PRIMARY KEY IDENTITY,

	NUMERO INT,
	NAME VARCHAR(25),
    TYPE_1 VARCHAR(8),
    TYPE_2 VARCHAR(8),
    TOTAL INT,
    HP INT,
    ATTACK INT,
    DEFENSE INT,
    SP_ATK INT,
    SP_DEF INT,
    SPEED INT,
    GENERATION INT,
    LEGENDARY VARCHAR(5)
)
GO

CREATE TABLE POKEMON(
	IDPOKEMON INT PRIMARY KEY IDENTITY,

	NAME_CUSTOM VARCHAR(25),
	NIVEL INT,

	ID_TRAINER INT NULL,
	ID_POKEDEX INT NOT NULL
)
GO

ALTER TABLE POKEMON ADD CONSTRAINT FK_POKEMON_TRAINER
FOREIGN KEY (ID_TRAINER)
REFERENCES TRAINER(IDTRAINER)
GO

ALTER TABLE POKEMON ADD CONSTRAINT FK_POKEMON_POKEDEX
FOREIGN KEY (ID_POKEDEX)
REFERENCES POKEDEX(IDPOKEDEX)
GO

SP_COLUMNS POKEDEX
GO

CREATE PROC CAMPEONATO
AS
	SELECT NOME, SEXO, PAIS, NAME_CUSTOM AS POKEMON, NIVEL, NAME, TYPE_1 AS TIPO
	FROM TRAINER T
	INNER JOIN POKEMON P
	ON T.IDTRAINER = P.ID_TRAINER
	INNER JOIN ENDERECO E
	ON T.IDTRAINER = E.ID_TRAINER
	INNER JOIN POKEDEX A
	ON A.IDPOKEDEX = P.ID_POKEDEX 
GO

